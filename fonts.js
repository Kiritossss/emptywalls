// ── Render engine ──
function renderFont(text, fontName) {
  const font = FONT_GLYPHS[fontName] || FONT_GLYPHS.block;
  const chars = text.toUpperCase().split('');
  const glyphs = chars.map(ch => font[ch] || font[' '] || ['   ','   ','   ','   ','   ']);
  const rows = glyphs[0].length;
  const lines = [];
  for (let r = 0; r < rows; r++) {
    lines.push(glyphs.map(g => (g[r] || '   ')).join(' '));
  }
  return lines.join('\n');
}
